namespace PhasmophobiaCompanion.Models
{
    /// <summary>
    ///     Класс AppShellCommon содержит атрибуты для локализации названий вкладок в приложении.
    /// </summary>
    public class AppShellCommon
    {
        /// <summary>
        ///     Название вкладки Проклятых предметов.
        /// </summary>
        public string CursedPossessions { get; set; }
        /// <summary>
        ///     Название вкладки Снаряжения.
        /// </summary>
        public string Equipments { get; set; }
        /// <summary>
        ///     Название вкладки Призраков.
        /// </summary>
        public string Ghosts { get; set; }
        /// <summary>
        ///     Название главной вкладки.
        /// </summary>
        public string Main { get; set; }
        /// <summary>
        ///     Название вкладки карт.
        /// </summary>
        public string Maps { get; set; }
    }
}